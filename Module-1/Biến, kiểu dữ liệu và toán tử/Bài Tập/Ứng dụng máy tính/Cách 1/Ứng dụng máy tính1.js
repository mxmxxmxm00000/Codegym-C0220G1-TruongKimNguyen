var num1= document.getElementById("num1");
var num2= document.getElementById("num2");
var result= document.getElementById("result");
function Add() {
    result.innerText= num1.value*1 + num2.value*1
}
function Sub() {
    result.innerText= num1.value - num2.value
}
function Mul() {
    result.innerText= num1.value * num2.value
}
function Div() {
    result.innerText= num1.value / num2.value
}




